if __name__ == "__main__":
    import matplotlib.pyplot as plt
    
    # 1. Generate Target Signal (Sharp peaks - ideal homopolymer transitions)
    np.random.seed(42)
    x = np.arange(200)
    true_signal = np.zeros_like(x, dtype=float)
    true_signal[40:50] = 50.0  # Sharp step
    true_signal[120:125] = 40.0 # Shorter sharp step
    
    # 2. Blurring factor (simulating ONT Sensor/Pore bandwidth limitation)
    psf = gaussian_psf_1d(size=15, sigma=3.0)
    
    # 3. Generate blurred signal
    blurred_signal = fftconvolve(true_signal, psf, mode='same')
    noisy_blurred = blurred_signal + np.random.normal(0, 1.0, len(x))
    
    # 4. Richardson-Lucy Restoration
    # First, move to positive space (noise may cross zero)
    pos_noisy, params = shift_to_positive(noisy_blurred, method="min_shift")
    
    # RL iteration
    restored_pos = richardson_lucy_1d(pos_noisy, psf, iterations=40)
    
    # Restore to original space
    restored = inverse_shift(restored_pos, params)
    
    plt.figure(figsize=(10, 5))
    plt.plot(x, true_signal, 'k--', label="Original Sharp (Unknown)", alpha=0.6)
    plt.plot(x, noisy_blurred, 'r-', label="ONT Blurred + Noise", alpha=0.7)
    plt.plot(x, restored, 'g-', label="RL Deconvolution (Restored)", linewidth=2)
    plt.title("Richardson-Lucy 1D Deconvolution Demo")
    plt.legend()
    plt.grid(True, alpha=0.3)
    plt.show()
